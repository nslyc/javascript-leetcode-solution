/**
 * Definition for a binary tree node.
 * function TreeNode(val) {
 *     this.val = val;
 *     this.left = this.right = null;
 * }
 */
/**
 * @param {TreeNode} root
 * @param {number} k
 * @return {boolean}
 */
var findTarget = function (root, k) {
    const arr = [];
    const stack = [root];
    while (stack.length) {
        let cur = stack.pop();
        arr.push(cur.val);
        if (cur.right) stack.push(cur.right);
        if (cur.left) stack.push(cur.left);
    }
    let map = {};
    for (let i = 0; i < arr.length; i++) {
        if (map[k - arr[i]] != null) return true;
        map[arr[i]] = arr[i];
    }
    return false;
};
